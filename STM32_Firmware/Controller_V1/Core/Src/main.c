/**
 * @brief UART Rx Callback for Joystick (USART3)
 */
void Joystick_RxCallback(UART_HandleTypeDef *huart)
{
    if (rx_byte == '\n' || rx_byte == '\r')
    {
      rx_index = 0;
    }
    else
    {
      if (rx_index < (sizeof(rx_buffer) - 1)) rx_buffer[rx_index++] = rx_byte;
      
      if (rx_index == 3)
      {
        rx_buffer[rx_index] = '\0';
        
        // 1. Emergency Check (Position [1])
        if (rx_buffer[1] == 'P') Motor_ProcessCommand('P');
        
        // 2. Connection Check (Position [2])
        Motor_SetConnectionStatus(rx_buffer[2] == 'C');
        
        // 3. Command Dispatch (Position [0])
        char cmd = rx_buffer[0];
        Motor_UpdateModeButton(cmd == 'M');
        Motor_UpdateSelectionButton(cmd == 'Y');
        Motor_UpdateControlModeButton(cmd == 'B');
        if (cmd != 'P') Motor_ProcessCommand(cmd);
        
        HAL_GPIO_TogglePin(GPIOA, GPIO_PIN_5);
        rx_index = 0;
      }
    }
    HAL_UART_Receive_IT(&huart3, &rx_byte, 1);
}
